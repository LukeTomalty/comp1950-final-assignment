<h1>About this Course</h1>

<p>    This hands-on course follows on from COMP 1850 Applied Web Development Level 1. Students who 
already under stand HTML and CSS will explore web development in more depth with new topics and 
techniques. Participants will gain a deeper understanding of the latest technologies including HTML5 
and CSS3 for developing web sites and applications. There is a focus on responsive design and mobile 
presentation through the use of Media Queries. Adding behavior to web sites using JavaScript 
frameworks such as jQuery is introduced. Labs and exercises focus on industry standard web 
development methodology. Students gain experience creating templates for content management systems 
and presentation. Additional topics will include: source control for managing your code, and web-based 
fonts for professional typography. By the end of this course successful students will be able to build 
dynamic and semantically marked-up content that is viewable on mobile and desktop screen sizes.</p>

<h2>Learning Outcomes</h2>
<ul>
    <li>To gain a deeper understanding of HTML, CSS and advanced topics in web development.</li>
    <li>To focus on best practices in web page and web site design.</li>
    <li>Add behaviour to any web page using JavaScript.</li>
    <li>Create dynamic content using JavaScript frameworks like jQuery.</li>
    <li>Employ Web Standards when building web sites for maximum compatibility.</li>
    <li>Understand basic development methodology.</li>
    <li>Use media queries to create responsive designs for mobile and desktop presentation.</li>
    <li>Use HTML5 technologies to present rich content such as video and audio.</li>
    <li>Create professional looking typography using web-based fonts.</li>
    <li>Understand source control using GIT.</li>
</ul>

